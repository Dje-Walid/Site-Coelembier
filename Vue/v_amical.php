                <h1>HISTOIRE</h1>
                <p>Dans les années 1980, création de l'Association Sportive et Culturelle de la Haie-Bertrand.
                <p>Plusieurs activités étaient pratiquées au sein de cette association (le football, la pétanque, la danse, la gymnastique, le tennis de table et l'art floral).
                <p>En 2000 Vincent Borg membre de l'association a demandé s'il pouvait prendre en charge la pétanque et en faire une amicale</p>
                <p>En Juin 2000, l'Amicale de Pétanque de la Haie-Bertrand était crée par Vincent Borg
                son activité principale est bien-sur la pratique de la pétanque</p>
                <p>Depuis cette date 4 pésidents se sont succédés : Vincent Borg, Jean-Claude Sivan, Pierre Brighton et Gérard Poli.</p>
                <p>Aujourd'hui la présidence est assurée par Thierry-Pierre Coelembier depuis le 25 mars 2021.</p>
                <p>Chaque président a fait vivre l'Amicale en fonction des évènements du moment. Rencontre amicale avec d'autres équipes des communes limitrophes, avant la mise en place
                et la création de clubs de pétanque affiliés à la fédération qui a compliqué les nouvelles rencontres.</p>
                <p> Des repas champêtres ont été organisés à plusieurs reprises.</p>
                <p>Depuis plusieurs années, l'A.P.H.B. a invité les handicapés de Toulouse Lautrec d'Aulnay sous Bois a venir jouer à la pétanque chez nous pour leur plus grand plaisir.
                Nous pratiquons notre sport sur les terrains de boules du Stade Criqui situé Avenue du 8 mai 1945 à Villepinte</p>
